
import java.util.Date;


public class Student {
    private String name;
    private Date birthDate;
   
    public Student(String name, Date birthDate) {
        this.name = name;
        this.birthDate = birthDate;
    }

    public String getName() {
        return name;
    }



    public Date getBirthDate() {
        return birthDate;
    }


   
       
 

            

                
            
    
}
